# Operator precedence: exponentiation > multipication or divison > addition or subtraction


x = 10
cube = x ** 3
print(cube)
div_decimal = x / 3
print(div_decimal)
div_int = x // 3
print(div_int)
remainder = x % 3 # modular operator
print(remainder)

# augmented operator
x += 3
print(x)
x -= 3
print(x)